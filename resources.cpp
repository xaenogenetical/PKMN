void printTextBoxBorder()
{
    setTileAtCursorTo(tile_list.at("box_corner_tl"));
    incrementCursor();
    for (int lr = 0; lr < SCREEN_SIZE_X_BOX - 2; lr++)
    {
        setTileAtCursorTo(tile_list.at("box_top"));
        incrementCursor();
    }
    setTileAtCursorTo(tile_list.at("box_corner_tr"));
    incrementCursor();
    for (int tb = 0; tb < 10; tb++)
    {
        setTileAtCursorTo(tile_list.at("box_left"));
        cursorTopLeftX += SCREEN_SIZE_X_BOX - 2;
        setTileAtCursorTo(tile_list.at("box_right"));
        incrementCursor();
    }
    for (int lr = 0; lr < SCREEN_SIZE_X_BOX - 2; lr++)
    {
        setTileAtCursorTo(tile_list.at("box_bottom"));
        incrementCursor();
    }
}

void clearTextBox()
{
    // move cursor to top of box
    setTileAtCursorTo(tile_list.at("blank"));
    incrementCursor();
    while (cursorTopLeftX == 0 || cursorTopLeftX == SCREEN_SIZE_X_BOX)
    {
        incrementCursor();
    }
}

