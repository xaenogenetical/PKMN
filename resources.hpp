class pokemon
{
private:
public:
      static move learnSet[];
      static int baseStats[];
      void *sprite;
      type type1;
      type type2;

      char sex;
      move knownMoves[4];
      int level;
      int IVs[5];
      int EVs[5];

      char *name;
      char *species;

      int statChanges[7];

      status affliction;

      int currHP;
      int maxHP;

      int stat_vals[5];

      pokemon(int dexNum)
      {
      }

      bool isFainted()
      {
            return currHP <= 0;
      }

      void level_up()
      {
            level++;
            /*increment stats*/
      }

      void heal()
      {
            currHP = maxHP;
      }

      void apply_status(status change)
      {
            if (change <= freeze)
            {
                  affliction = change;
                  return;
            }
            else
            {
            lap:
                  int ind;
                  if (change < atk_dwn) // buff
                  {
                        ind = change - atk_up;
                        statChanges[ind] = statChanges[ind] < 6 ? statChanges[ind] + 1 : statChanges[ind];
                  }
                  else // debuff
                  {
                        ind = change - atk_dwn;
                        statChanges[ind] = statChanges[ind] > -6 ? statChanges[ind] - 1 : statChanges[ind];
                  }
            }
      }

      int fetchReal(stats stat)
      {
            int val;
            if (stat < accuracy)
            {
                  val = stat_vals[stat] * (((double)modifiedStat(statChanges[stat])) / 100.0);
                  switch (stat)
                  {
                  case attack:
                        if (affliction == burn)
                        {
                              val >>= 1; // same as halving
                        }
                        break;
                  case speed:
                        if (affliction == paralyze)
                        {
                              val /= 3;
                        }
                        break;
                  }
            }
      }

      ~pokemon()
      {
      }
};

